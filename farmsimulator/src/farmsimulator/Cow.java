package farmsimulator;

public class Cow extends Animal {
	
	public String cowName = null;
	public String cowType = null;
	public int cowAge = 0;
	
	public Cow () {
		
	}
	
	public String getCowName() {
		return cowName;
	}
	
	public String getCowType() {
		return cowType;
	}
	
	public int getCowAge() {
		return cowAge;
	}

}
