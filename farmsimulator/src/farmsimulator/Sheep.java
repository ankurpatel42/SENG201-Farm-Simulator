package farmsimulator;

public class Sheep extends Animal {
	
	public String sheepName = null;
	public String sheepType = null;
	public int sheepAge = 0;
	
	public Sheep () {
		
	}
	
	public String getSheepName() {
		return sheepName;
	}
	
	public String getSheepType() {
		return sheepType;
	}
	
	public int getSheepAge() {
		return sheepAge;
	}

}
