package farmsimulator;

public class Farmer {
	
	private String farmerName = null;
	private int actionsLeft = 0;
	private int farmerAge = 0;
	
	public Farmer () {
		
	}
	
	public String getFarmerName() {
		return farmerName;
	}
	
	public int getActionsLeft() {
		return actionsLeft;
	}
	
	public int getAge() {
		return farmerAge;
	}
	
}
