package farmsimulator;

public class Pig extends Animal {
	
	public String pigName = null;
	public String pigType = null;
	public int pigAge = 0;
	
	public Pig () {
		
	}
	
	public String getPigName() {
		return pigName;
	}
	
	public String getPigType() {
		return pigType;
	}
	
	public int getPigAge() {
		return pigAge;
	}

}
